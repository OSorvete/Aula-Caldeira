package aula06;

public class Agenda {
    String numDeTelefone;
    String nome;
    String endereco;
    String localidade;

    public Agenda (String numDeTelefone, String nome, String endereco, String localidade){
        this.numDeTelefone = numDeTelefone;
        this.nome = nome;
        this.endereco = endereco;
        this.localidade = localidade;
    }
}
